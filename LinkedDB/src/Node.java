public class Node {
	
	@SuppressWarnings("null")
	private int dato = (Integer) null;
	private Node next = null;
	
	
	public int getDato() {
		return dato;
	}
	public void setDato(int dato) {
		this.dato = dato;
	}
	public Node getNext() {
		return next;
	}
	public void setNext(Node next) {
		this.next = next;
	}

}
